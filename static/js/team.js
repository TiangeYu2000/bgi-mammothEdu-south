$(document).ready(function () {
  const $studentCard = $(".student-card");
  const $teamDescription = $(".team-description");

  // When hovering over a tree node
  $(".tree__node").on("mouseover", function () {
    const studentKey = $(this).data("student");
    const student = stuDict[studentKey];

    if (student) {
      // Populate the student card with student data
      const studentInfo = `
        <div class="student-card-content">
          <img src="${student.photo}" alt="${student.name}" class="student-card-img">
          <h2 class="student-card-name">${student.name}</h2>
          <p class="student-card-description">${student.description}</p>
        </div>
      `;

      // Inject student info into the student card
      $studentCard.html(studentInfo);

      // Hide the team description and show the student card
      $teamDescription.hide();
      $studentCard.addClass("show");
    }
  });

  // When the mouse leaves a node, hide the student card and show the description
  $(".tree__node").on("mouseleave", function () {
    $studentCard.removeClass("show");
    $teamDescription.show();
  });
});


